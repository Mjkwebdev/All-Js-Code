// Loops
for(let i =0 ; i<5; i++){
    console.log("Hello World");
}
// for : keyword
//i=0 initialization
//i<5 condition
//i++ increment
let j = 0;
while(j < 5){
console.log("Hello World");
j++;
}
// same logic with while loop
// Do while 
let k=1;
do{
    console.log("Hello World");
    k++;
}while(k <5);