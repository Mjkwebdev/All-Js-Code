//  1.1 Arithmetic operators 
 let x= 5;
 let y=6;
 console.log(x + y);  //add
 console.log(x - y);  //sub
 console.log(x * y);  //mul
 console.log(x / y);  //div
 console.log(x % y);  //mod //remainder
 console.log(x ** y); // exponential 

 console.log(++x); //pre-increment 

 console.log(x++); // post-increment
